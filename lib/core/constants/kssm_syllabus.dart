  /// Generates the syllabus string for Gemini.
  /// If [form] is provided, returns only that Form's syllabus.
  /// Otherwise, returns the entire KSSM syllabus.
  static String getPrompt({int? form}) {
    StringBuffer buffer = StringBuffer();
    buffer.writeln("KSSM Mathematics Syllabus (Malaysia)");

    structure.forEach((formLevel, chapters) {
      if (form != null && form != formLevel) return;

      buffer.writeln("\nFORM $formLevel");
      for (var chapter in chapters) {
        buffer.writeln("Chapter ${chapter.id}: ${chapter.title}");
        if (chapter.subtopics.isNotEmpty) {
          chapter.subtopics.forEach((subId, subTitle) {
            buffer.writeln("  ${chapter.id}.$subId $subTitle");
          });
        }
      }
    });

    return buffer.toString();
  }
